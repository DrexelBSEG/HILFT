function pow = predict_hp_cool_power_new(T_loa_in, T_sou_in, y_speed)

    % Model Parameters
    coeff = [2.88488449e+01, 1.48678462e+01, -5.63445297e+02,...
             -4.35811981e-01, -6.30404997e-01, 4.19024672e+00,...
             4.36505564e-02, 3.34750130e+01, ...
             1.05415812e+03,...
             -3.10434539e+02];
    
    % Features
    x1 = T_loa_in;
    x2 = T_sou_in;
    x3 = y_speed;
    
    % Create a feature matrix including all terms and a column of ones for the intercept
    X = [x1, x2, x3,...
         x1.^2, x1.*x2, x1.*x3,...
         x2.^2, x2.*x3,...
         x3.^2,...
         ones(size(x1,1),1)];
    
    % Use matrix multiplication for applying coefficients
    pow = X * coeff';
    
end
